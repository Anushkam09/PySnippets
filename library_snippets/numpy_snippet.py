import numpy as np

def add(arr1, arr2):
    return np.add(arr1, arr2)

def subtract(arr1, arr2):
    return np.subtract(arr1, arr2)

def multiply(arr1, arr2):
    return np.multiply(arr1, arr2)

def divide(arr1, arr2):
    return np.divide(arr1, arr2)

def power(arr1, arr2):
    return np.power(arr1, arr2)

def sqrt(arr):
    return np.sqrt(arr) 

def log(arr):
    return np.log(arr)  

def exp(arr):
    return np.exp(arr)

def sin(arr):
    return np.sin(arr)

def cos(arr):
    return np.cos(arr)

def tan(arr):
    return np.tan(arr)

def abs(arr):
    return np.abs(arr)